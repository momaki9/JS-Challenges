var addFunction = function(num1, num2) {
    return num1 + num2;
};

var subtractFunction = function(num1, num2) {
    return num1 - num2;
};

var multiplyFunction = function(num1, num2) {
    return num1 * num2;
};

var divideFunction = function(num1, num2) {
    return num1 / num2;
};

console.log(addFunction(2, 4));
console.log(subtractFunction(78, 51));
console.log(multiplyFunction(33, 6));
console.log(divideFunction(81, 9));